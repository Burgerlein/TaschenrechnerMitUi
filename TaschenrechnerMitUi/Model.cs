namespace TaschenrechnerMitUi;

public class Model
{
    public static int Addition(int zahl1, int zahl2)
    {
        return zahl1 + zahl2;
    }
    public static int Subtraction(int zahl1, int zahl2)
    {
        return zahl1 - zahl2;
    }
    public static int Multiplication(int zahl1, int zahl2)
    {
        return zahl1 * zahl2;
    }
    public static int Division(int zahl1, int zahl2)
    {
        return zahl1 / zahl2;
    }
}